package ivms.sysware.com.ivmsmex.services.security;

public class Vehicle {
    private String vehiclePlates;
    private String vehicleType;
    private String vehicleModel;
    private String vehicleResponsible;
    private String vehicleResponsibleEmail;
    private String vehicleZone;
    private String vehicleLocation;
    private String vehicleId;

    public String getVehicleId() {
        return vehicleId;
    }

    public void setVehicleId(String vehicleId) {
        this.vehicleId = vehicleId;
    }

    public String getVehiclePlates() {
        return vehiclePlates;
    }

    public void setVehiclePlates(String vehiclePlates) {
        this.vehiclePlates = vehiclePlates;
    }

    public String getVehicleType() {
        return vehicleType;
    }

    public void setVehicleType(String vehicleType) {
        this.vehicleType = vehicleType;
    }

    public String getVehicleModel() {
        return vehicleModel;
    }

    public void setVehicleModel(String vehicleModel) {
        this.vehicleModel = vehicleModel;
    }

    public String getVehicleResponsible() {
        return vehicleResponsible;
    }

    public void setVehicleResponsible(String vehicleResponsible) {
        this.vehicleResponsible = vehicleResponsible;
    }

    public String getVehicleResponsibleEmail() {
        return vehicleResponsibleEmail;
    }

    public void setVehicleResponsibleEmail(String vehicleResponsibleEmail) {
        this.vehicleResponsibleEmail = vehicleResponsibleEmail;
    }

    public String getVehicleZone() {
        return vehicleZone;
    }

    public void setVehicleZone(String vehicleZone) {
        this.vehicleZone = vehicleZone;
    }

    public String getVehicleLocation() {
        return vehicleLocation;
    }

    public void setVehicleLocation(String vehicleLocation) {
        this.vehicleLocation = vehicleLocation;
    }
}
